#define BACKSPACE 0x100
#define CRTPORT 0x3d4
static ushort *crt = (ushort*)P2V(0xb8000);  // CGA memory

/*
  this method actually prints the character to the xv6 screen
*/
static void
cgaputc(int c)
{
  int pos;

  // Cursor position: col + 80*row.
  outb(CRTPORT, 14);
  pos = inb(CRTPORT+1) << 8;
  outb(CRTPORT, 15);
  pos |= inb(CRTPORT+1);

  switch(c) {
    case '\n':
      pos += 80 - pos%80;
      break;
    case BACKSPACE:
      if(pos > 0) --pos;
      break;
    case LEFT_ARROW:
      pos--;
      //crt[pos] = crt[pos] | 0x0700;
      break;
    default:
      crt[pos++] = (c&0xff) | 0x0700;  // black on white
  }

  // if(c == '\n')
  //   pos += 80 - pos%80;
  // else if(c == BACKSPACE){
  //   if(pos > 0) --pos;
  // } else
  //   crt[pos++] = (c&0xff) | 0x0700;  // black on white

  if(pos < 0 || pos > 25*80)
    panic("pos under/overflow");

  if((pos/80) >= 24){  // Scroll up.
    memmove(crt, crt+80, sizeof(crt[0])*23*80);
    pos -= 80;
    memset(crt+pos, 0, sizeof(crt[0])*(24*80 - pos));
  }

  outb(CRTPORT, 14);
  outb(CRTPORT+1, pos>>8);
  outb(CRTPORT, 15);
  outb(CRTPORT+1, pos);
  if (c == BACKSPACE)
    crt[pos] = ' ' | 0x0700;
}

/*
    wrting things to the linux screen and the calls cgaputc
*/
void
consputc(int c)
{
  if(panicked){
    cli();
    for(;;)
      ;
  }
  switch(c){
    case BACKSPACE:
    uartputc('\b'); uartputc(' '); uartputc('\b'); // uart is writing to the linux shell
      break;
    case LEFT_ARROW:
      uartputc('\b');
      break;
    default:
      uartputc(c);
  }
  cgaputc(c);
}

